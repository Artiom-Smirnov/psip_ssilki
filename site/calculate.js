function calculateF(t) {
    try {
        // Проверка на деление на ноль
        if (t === 8) {
            throw "Ошибка: деление на ноль!";
        }

        // Проверка на корень из отрицательного числа
        if (t < -7) {
            throw "Ошибка: извлечение корня из отрицательного числа!";
        }

        // Расчет значения по формуле
        let result = (-t + Math.sqrt(7 + t)) / 8 - Math.abs(t);

        // Возвращаем результат
        return result;
    } catch (error) {
        // Обработка исключительных ситуаций с выводом в alert
        alert(error);
    }
}
